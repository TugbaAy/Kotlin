package VariableTypes

fun main(args: Array<String>) {


    // Number types
    // int,float,double,byte,short,long

    //byte - 8 bit

    var byteMin : Byte = Byte.MIN_VALUE
    var byteMax : Byte = Byte.MAX_VALUE

    print("Byte Min Value " +byteMin)
    print("Byte Max Value " +byteMax)

    //short - 16 bit

    var shortMin : Short = Short.MIN_VALUE
    var shortMax : Short = Short.MAX_VALUE

    print("Short Min Value " +shortMin)
    print("Short Max Value " +shortMax)

    // int - 32 bit

    var IntMin : Int = Int.MIN_VALUE
    var IntMax : Int = Int.MAX_VALUE

    print("Int Min Value " +IntMin)
    print("Int Max Value " +IntMax)

    // float - 32 bit

    var FloatMin : Float = Float.MIN_VALUE
    var FloatMax : Float = Float.MAX_VALUE

    print("Int Min Value " +FloatMin)
    print("Int Max Value " +FloatMax)

    // Long - 64 bit

    var LongMin : Long = Long.MIN_VALUE
    var LongMax : Long = Long.MAX_VALUE

    print("Long Min Value $LongMin")
    print("Long Max Value $LongMax")

    // Double - 64 bit

    var DoubleMin : Double = Double.MIN_VALUE
    var DoubleMax : Double = Double.MAX_VALUE

    print("Long Min Value $DoubleMin")
    print("Long Max Value $DoubleMax")

}